import { apiRequest, handleErrors } from "../../../../Utils/apiHandler";

const baseRoutes = "/user/profile/skills";

//get -- post
//add --post
//delete --post
//update --post

export const getSkillsHandler = async (data, setIsLoading, showAlert) => {
  const url = `${baseRoutes}/get`;
  const obj = data;

  setIsLoading(true);
  const token = localStorage.getItem("token");

  try {
    const response = await apiRequest(url, obj, token, "post");
    const data = response.data;
    return data;
  } catch (e) {
    handleErrors(e, showAlert);
  } finally {
    setIsLoading(false);
  }
};

